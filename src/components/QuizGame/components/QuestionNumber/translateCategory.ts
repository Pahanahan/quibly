export const translateCategory = (category: string) => {
  if (category === "geography") {
    return "География";
  }
  if (category === "history") {
    return "История";
  }
  if (category === "economics") {
    return "Экономика";
  }
  if (category === "space") {
    return "Космос";
  }
  if (category === "politics") {
    return "Политика";
  }
  if (category === "medicine") {
    return "Медицина";
  }
  if (category === "biology") {
    return "Биология";
  }
  if (category === "art") {
    return "Исскуство";
  }
  if (category === "music") {
    return "Музыка";
  }
  if (category === "literature") {
    return "Литература";
  }
  if (category === "filmsSeries") {
    return "Фильмы и сериалы";
  }
  if (category === "maths") {
    return "Математика";
  }
  if (category === "physics") {
    return "Физика";
  }
  if (category === "chemistry") {
    return "Химия";
  }
  if (category === "sport") {
    return "Спорт";
  }
  if (category === "english") {
    return "Английский язык";
  }
  if (category === "football") {
    return "Футбол";
  }
  if (category === "boxing") {
    return "Бокс";
  }
  if (category === "weapons") {
    return "Оружие";
  }
  if (category === "fashion") {
    return "Мода и стиль";
  }
  if (category === "capitals") {
    return "Мировые столицы";
  }
  if (category === "cars") {
    return "Автомобили";
  }
  if (category === "cooking") {
    return "Кухня и готовка";
  }
  if (category === "religion") {
    return "Религии";
  }
  if (category === "philosophy") {
    return "Философия";
  }
};
