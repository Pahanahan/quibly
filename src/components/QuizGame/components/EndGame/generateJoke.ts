export const generateJoke = (place: number, avatar: string) => {
  if (place === 0) {
    return "Поздравляем! Ты занял первое место!";
  } else if (place === 1) {
    return "Второе место тоже почетное!";
  } else if (place === 2) {
    return "В следующий раз получишь что-то получше бронзы!";
  } else {
    if (avatar === "ape") {
      return "Ты не проиграл… ты просто эволюционировал не в ту сторону.";
    } else if (avatar === "bird") {
      return "Ты был так близко к победе, что даже птицы сверху начали хлопать крыльями от испанского стыда.";
    } else if (avatar === "bull") {
      return "Результат как у быка на льду — мощно, но абсолютно бесполезно.";
    } else if (avatar === "dog") {
      return "Ты занял место ниже бронзы, но зато хороший мальчик. Кто хороший мальчик? Ты хороший мальчик!";
    } else if (avatar === "camel") {
      return "Ты шёл к победе как верблюд через пустыню… 3 часа и не туда.";
    } else if (avatar === "cat") {
      return "Ты не проиграл, ты просто скинул викторину со стола, потому что она слишком мешала.";
    } else if (avatar === "horse") {
      return "Ты ворвался в игру как конь, а финишировал как пони на пенсии.";
    } else if (avatar === "rabbit") {
      return "Ты был быстрый как кролик, но умный как кролик. В этом и проблема.";
    } else if (avatar === "cow") {
      return "Ты явно не сделал «му-му» в нужный момент.";
    } else if (avatar === "duck") {
      return "Ты летел к победе как утка на юг… но случайно свернул в суп.";
    } else if (avatar === "elephant") {
      return "Ты топтал игру как слон, но так аккуратно, что она даже не заметила.";
    } else if (avatar === "fish") {
      return "Ты плыл к победе… но всплыл слишком рано.";
    } else if (avatar === "goose") {
      return "Ты крякнул громче всех, клюнул воздух, побегал кругами… В общем, классика жанра. Победа просто испугалась и убежала.";
    } else if (avatar === "giraffe") {
      return "Ты — жираф, который увидел финиш, но решил: «Слишком низко наклоняться, не моё» — и прошёл мимо.";
    } else if (avatar === "lion") {
      return "Ты рычал как лев, отвечал как котёнок после наркоза.";
    } else if (avatar === "mouse") {
      return "Ты кликал быстрее всех… но клетки мозга не успевали.";
    } else if (avatar === "turtle") {
      return "Ты дошёл до финиша с философией черепахи: главное не скорость, а зачем вообще это всё?";
    } else if (avatar === "tiger") {
      return "Ты был опасен для всех… но вопросы оказались с дрессировщиком.";
    } else if (avatar === "zebra") {
      return "Твоё место не чёрное и не белое. Оно ниже.";
    }
  }
};
